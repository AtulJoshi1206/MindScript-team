"""## Interactive Prediction Function

### Subtask:
Create a function that takes user input text, preprocesses it using the saved TF-IDF vectorizer, uses the saved Logistic Regression model to predict the depression probability, converts it to a score, and categorizes it into a depression level. This function will then display the score and level.

**Reasoning**:
I need to import joblib to load the saved model and vectorizer, then define the `predict_depression` function that preprocesses the input text, vectorizes it, predicts the probability, converts it to a score and category, and finally prints the results.
"""

import joblib
import string

# Load the saved model and vectorizer
loaded_model = joblib.load('logistic_regression_model.pkl')
loaded_vectorizer = joblib.load('tfidf_vectorizer.pkl')

def predict_depression(user_input_text):
    """
    Predicts the depression score and level for a given user input text.

    Args:
        user_input_text (str): The text input from the user.

    Returns:
        None: Prints the depression score and level directly.
    """
    # Preprocess the user input text
    processed_text = user_input_text.lower()
    processed_text = processed_text.translate(str.maketrans('', '', string.punctuation))
    processed_text = processed_text.strip().replace(r'\s+', ' ', regex=True)

    # Transform the preprocessed text using the loaded TF-IDF vectorizer
    text_vectorized = loaded_vectorizer.transform([processed_text])

    # Predict the probability of depression using the loaded model
    # predict_proba returns probabilities for both classes (0 and 1). We need the probability of class 1.
    depression_probability = loaded_model.predict_proba(text_vectorized)[0][1]

    # Convert probability to a depression score (0-100)
    depression_score = predict_and_score(depression_probability)

    # Categorize the depression level
    depression_level = categorize_depression_level(depression_score)

    print(f"\nUser Input: '{user_input_text}'")
    print(f"Depression Probability: {depression_probability:.2f}")
    print(f"Depression Score (0-100): {depression_score}")
    print(f"Depression Level: {depression_level}")

print("Interactive prediction function `predict_depression` has been defined.")

