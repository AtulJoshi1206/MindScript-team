"""## Define Scoring and Categorization Functions

### Subtask:
Implement functions to convert the model's predicted probabilities into a depression score (0-100) and categorize these scores into 'Low', 'Moderate', or 'High' depression levels based on predefined thresholds.

**Reasoning**:
I will define two Python functions as per the instructions: one to convert a probability to a depression score out of 100, and another to categorize this score into 'Low', 'Moderate', or 'High' levels with specified thresholds. Both functions will include comprehensive docstrings.
"""

def predict_and_score(probability):
    """
    Converts a depression probability (0-1) into a depression score (0-100).

    Args:
        probability (float): The predicted probability of depression, a value between 0 and 1.

    Returns:
        int: A depression score on a scale of 0 to 100.
    """
    return int(probability * 100)

def categorize_depression_level(score):
    """
    Categorizes a depression score (0-100) into 'Low', 'Moderate', or 'High' levels.

    Args:
        score (int): The depression score, an integer between 0 and 100.

    Returns:
        str: The categorized depression level ('Low', 'Moderate', or 'High').
    """
    if 0 <= score <= 33:
        return 'Low'
    elif 34 <= score <= 66:
        return 'Moderate'
    elif 67 <= score <= 100:
        return 'High'
    else:
        return 'Invalid Score'

print("Functions `predict_and_score` and `categorize_depression_level` have been defined.")

